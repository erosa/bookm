<h1>exmeamente.ws/bm/about</h1>

<p>In late 2008 I was looking for a plainer, slicker alternative to Delicious.com. All I wanted was to have my bookmarks accessible from anywhere. I didn't need tags or social networking features or anything. I came across <a href="http://www.emailaddresses.com/email_bookmarks.htm">quite a few</a>. Some were <a href="http://spurl.net/">the same thing</a>, some had <a href="http://www.bookmax.net/">weird ladies and odd books</a>, some were <a href="http://myvmarks.com/">written in ColdFusion</a>, none were what I was looking for.</p>

<p>So sat down and wrote my own. It's not the coolest thing in the world, it took me like 3 hours to write, but it scratched my itch. Maybe it will scratch yours, too.</p>

<p>If you want my code, you can <a href="bm-1.0.tar.gz">have it</a>. Questions or comments, email me, elr at exmeamente dot ws.</p>

